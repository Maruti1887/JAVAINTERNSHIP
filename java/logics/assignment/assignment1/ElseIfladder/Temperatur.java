class Temperatur
{
	public static void main(String[] args) 
	{
		int a=50;
		if(a>=40)
			System.out.println("Very hot");
		else if(a>=30)
			System.out.println(" Hot");
		else if(a>=20)
			System.out.println("Warm");
		else if(a>=10)
			System.out.println("Cold");
		else
			System.out.println(" very Cold");
	}
}
